#include <iostream>
using namespace std;

int main() {
    int nota;

    cout << "Ingrese la nota: ";
    cin >> nota;

    if (nota = 50)
        cout << "excelente";
    else if (nota = 30)
        cout << "pasa raspando pelao";
    else if (nota >= 70)
        cout << "perdió parcero";

    return 0;
}
